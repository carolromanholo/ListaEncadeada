public class Node {
    private String valor;
    private Node proximo;



    public void setproximo(Node proximo) {
        this.proximo = proximo;
    }

    public Node getproximo() {
        return proximo;
    }


    public void setvalor(String valor) {
        this.valor = valor;
    }

    public String getvalor() {
        return valor;
    }
}
